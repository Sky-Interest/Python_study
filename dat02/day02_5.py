a = 10
b = 6
"""
a&b
   a  0000 1010
   b  0000 0110
=====================  两个位都为1时，结果才为1
   &  0000 0010

a|b
   a  0000 1010
   b  0000 0110
=====================  两个位都为0时，结果才为0
   |  0000 1110

a^b
   a  0000 1010
   b  0000 0110
=====================  两个位相同为0，相异为1
   ^  0000 1100
   
~a
   a  0000 1010
=====================  取反后 +1
  反  1111 0101
  ~a  1000 1011  
  
~b
   b  0000 0110
=====================  取反后 +1
  反  1111 1001
  ~b  1000 0111 

a<<b
   a  0000 1010
   b  0000 0110
=====================  各二进位全部左移6位，高位丢弃，低位补0
  <<  1000 0000
  
a>>b
   a  0000 1010
   b  0000 0110
=====================  各二进位全部右移6位
  >>  0000 0000

4 >> 2
   4  0000 0100
   2  0000 0010
=====================  4右移2位
  >>  0000 0001

4 << 2
   4  0000 0100
   2  0000 0010
=====================  4左移2位
  >>  0001 0000

"""

print(f"{a} & {b} = {a&b}")
print(f"{a} | {b} = {a|b}")
print(f"{a} ^ {b} = {a^b}")
print(f"~{a} = {~a} ")
print(f"~{b} = {~b} ")
print(f"{a} >> {b} = {a>>b}")
print(f"{a} << {b} = {a<<b}")
print(f"{4} >> {2} = {4>>2}")
print(f"{4} << {2} = {4<<2}")
