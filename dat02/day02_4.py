a = 10
b = 15
print(f"{a} == {b} = {a==b}")
print(f"{a} != {b} = {a!=b}")
print(f"{a} > {b} = {a>b}")
print(f"{a} < {b} = {a<b}")
print(f"{a} >= {b} = {a>=b}")
print(f"{a} <= {b} = {a<=b}")
